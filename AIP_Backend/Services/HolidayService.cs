        public async Task<HolidayRequestResponseDto> SubmitHolidayRequestAsync(HolidayRequestSubmissionDto request, int employeeId, string currentUserId)
        {
            try
            {
                _logger.LogInformation("Submitting holiday request for employee {EmployeeId}", employeeId);

                // Validate request
                var validationErrors = ValidateHolidayRequest(request);
                if (validationErrors.Any())
                {
                    throw new ValidationException($"Holiday request validation failed: {string.Join(", ", validationErrors)}");
                }

                // Check for conflicts
                var conflicts = await CheckHolidayConflictsAsync(request.StartDate, request.EndDate, employeeId);
                if (conflicts.Any())
                {
                    _logger.LogWarning("Holiday request conflicts detected for employee {EmployeeId}", employeeId);
                }

                // Create holiday request (placeholder - implement with your repository)
                var holidayRequest = new HolidayRequestResponseDto
                {
                    Id = 1, // This would be generated by your repository
                    EmployeeId = employeeId,
                    EmployeeName = "User", // This would come from user service
                    EmployeeEmail = "user@company.com", // This would come from user service
                    StartDate = request.StartDate,
                    EndDate = request.EndDate,
                    NumberOfDays = request.NumberOfDays,
                    HolidayType = request.HolidayType,
                    Reason = request.Reason,
                    Comments = request.Comments,
                    Status = "Pending",
                    CreatedAt = DateTime.UtcNow,
                    UpdatedAt = DateTime.UtcNow,
                    IsHalfDay = request.IsHalfDay,
                    HalfDayType = request.HalfDayType
                };

                // Get user details for email notification (using EmployeeId field as legacy mapping)
                var allUsers = _userManager.Users.ToList();
                var user = allUsers.FirstOrDefault(u => u.EmployeeId == employeeId);
                if (user != null && !string.IsNullOrWhiteSpace(user.Email))
                {
                    // Send notification to manager
                    try
                    {
                        var managerEmail = "manager@company.com"; // This would come from your user/manager relationship
                        await _holidayEmailService.SendHolidayRequestSubmittedNotificationAsync(holidayRequest, user, managerEmail);
                        _logger.LogInformation("Holiday request submitted notification sent to manager");
                    }
                    catch (Exception emailEx)
                    {
                        _logger.LogWarning(emailEx, "Failed to send holiday request submitted notification");
                    }

                    // Send conflict notification if conflicts exist
                    if (conflicts.Any())
                    {
                        try
                        {
                            await _holidayEmailService.SendHolidayRequestConflictNotificationAsync(holidayRequest, user, conflicts.ToList());
                            _logger.LogInformation("Holiday request conflict notification sent to user");
                        }
                        catch (Exception emailEx)
                        {
                            _logger.LogWarning(emailEx, "Failed to send holiday request conflict notification");
                        }
                    }
                }

                _logger.LogInformation("Holiday request submitted successfully for employee {EmployeeId}", employeeId);
                return holidayRequest;
            }
            catch (Exception ex)
            {
                _logger.LogError(ex, "Error submitting holiday request for employee {EmployeeId}", employeeId);
                throw;
            }
        }

