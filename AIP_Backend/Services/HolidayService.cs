        #region Private Helper Methods

        private List<string> ValidateHolidayRequest(HolidayRequestSubmissionDto request)
        {
            var errors = new List<string>();

            if (request.StartDate < DateTime.Today)
            {
                errors.Add("Start date cannot be in the past");
            }

            if (request.EndDate < request.StartDate)
            {
                errors.Add("End date cannot be before start date");
            }

            if (request.NumberOfDays <= 0)
            {
                errors.Add("Number of days must be greater than 0");
            }

            if (string.IsNullOrWhiteSpace(request.HolidayType))
            {
                errors.Add("Holiday type is required");
            }

            if (request.IsHalfDay && string.IsNullOrWhiteSpace(request.HalfDayType))
            {
                errors.Add("Half day type is required when requesting half day");
            }

            return errors;
        }

        #endregion
